async def scheduled_notifications():
    while True:
        for chat_id in ADMIN_IDS:
            choice = random.choice(["fate", "advice", "support", "fact", "letter"])
            if choice == "fate":
                phrase = random.choice(fate_phrases)
                await bot.send_message(chat_id, f"üîÆ –ù–∞–ø–æ–º–∏–Ω–∞–Ω–∏–µ: {phrase}")
            elif choice == "advice":
                phrase = random.choice(cat_advice)
                await bot.send_message(chat_id, f"üêæ –°–æ–≤–µ—Ç: {phrase}")
            elif choice == "support":
                phrase = random.choice(support_phrases)
                await bot.send_message(chat_id, f"üíñ –ü–æ–¥–¥–µ—Ä–∂–∫–∞: {phrase}")
            elif choice == "fact":
                fact = random.choice(cat_facts)
                gif = random.choice(cat_gifs)
                await bot.send_animation(chat_id, gif, caption=f"üê± –§–∞–∫—Ç: {fact}")
            elif choice == "letter":
                for phrase in farida_letter:
                    await bot.send_message(chat_id, f"‚úâÔ∏è {phrase}")
        await asyncio.sleep(4 * 60 * 60)  # 4 —á–∞—Å–∞

